package com.p2p.pojo;

import java.io.Serializable;

import com.baomidou.mybatisplus.annotations.TableField;

public class Transfer implements Serializable{
	@TableField("t_id")
	private Integer tid;
	@TableField("t_suid")
	private Integer tsuid;
	@TableField("t_suids")
	private Integer tsuids;
	@TableField("t_money")
	private double tmoney;
	@TableField("t_time")
	private String ttime;
	@TableField("t_state")
	private Integer tstate;
	@TableField("t_order")
	private String torder;
	@TableField("t_charge")
	private double tcharge;
	@TableField("t_ip")
	private Integer tip;
	private String tsuname;
	
	private String tsunames;
	
	public Transfer() {
		super();
	}

	public Transfer(Integer tid, Integer tsuid, Integer tsuids, double tmoney, String ttime, Integer tstate,
			String torder, double tcharge, String tsuname, String tsunames,Integer tip) {
		super();
		this.tid = tid;
		this.tsuid = tsuid;
		this.tsuids = tsuids;
		this.tmoney = tmoney;
		this.ttime = ttime;
		this.tstate = tstate;
		this.torder = torder;
		this.tcharge = tcharge;
		this.tsuname = tsuname;
		this.tsunames = tsunames;
		this.tip = tip;
	}


	@Override
	public String toString() {
		return "Transfer [tid=" + tid + ", tsuid=" + tsuid + ", tsuids=" + tsuids + ", tmoney=" + tmoney + ", ttime="
				+ ttime + ", tstate=" + tstate + ", torder=" + torder + ", tcharge=" + tcharge + ", tip=" + tip
				+ ", tsuname=" + tsuname + ", tsunames=" + tsunames + "]";
	}

	public Integer getTid() {
		return tid;
	}

	public void setTid(Integer tid) {
		this.tid = tid;
	}

	public Integer getTsuid() {
		return tsuid;
	}

	public void setTsuid(Integer tsuid) {
		this.tsuid = tsuid;
	}

	public Integer getTsuids() {
		return tsuids;
	}

	public void setTsuids(Integer tsuids) {
		this.tsuids = tsuids;
	}

	public double getTmoney() {
		return tmoney;
	}

	public void setTmoney(double tmoney) {
		this.tmoney = tmoney;
	}

	public String getTtime() {
		return ttime;
	}

	public void setTtime(String ttime) {
		this.ttime = ttime;
	}

	public Integer getTstate() {
		return tstate;
	}

	public void setTstate(Integer tstate) {
		this.tstate = tstate;
	}

	public String getTorder() {
		return torder;
	}

	public void setTorder(String torder) {
		this.torder = torder;
	}

	public double getTcharge() {
		return tcharge;
	}

	public void setTcharge(double tcharge) {
		this.tcharge = tcharge;
	}

	public String getTsuname() {
		return tsuname;
	}

	public void setTsuname(String tsuname) {
		this.tsuname = tsuname;
	}

	public String getTsunames() {
		return tsunames;
	}

	public void setTsunames(String tsunames) {
		this.tsunames = tsunames;
	}

	public Integer getTip() {
		return tip;
	}

	public void setTip(Integer tip) {
		this.tip = tip;
	}
	
}
