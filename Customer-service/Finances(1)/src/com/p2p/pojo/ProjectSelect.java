package com.p2p.pojo;

import java.io.Serializable;
/**
 * 关于标种的自定义实体类
 * 操作人:汪栋才
 * 操作时间:2018-1-2
 * */
@SuppressWarnings("serial")
public class ProjectSelect implements Serializable{
	
	private String progress; //进度
	
	private String borrow_interest_rate; //收益率
	
	private String borrow_money; //金额度
	
	private String borrow_status; //项目情况
	
	private String leftday; //时限
	
	private String pagetype; //标类型
	
	public ProjectSelect() {
	}

	public ProjectSelect(String progress, String borrow_interest_rate, String borrow_money, String borrow_status,
			String leftday, String pagetype) {
		super();
		this.progress = progress;
		this.borrow_interest_rate = borrow_interest_rate;
		this.borrow_money = borrow_money;
		this.borrow_status = borrow_status;
		this.leftday = leftday;
		this.pagetype = pagetype;
	}

	public String getProgress() {
		return progress;
	}

	public void setProgress(String progress) {
		this.progress = progress;
	}

	public String getBorrow_interest_rate() {
		return borrow_interest_rate;
	}

	public void setBorrow_interest_rate(String borrow_interest_rate) {
		this.borrow_interest_rate = borrow_interest_rate;
	}

	public String getBorrow_money() {
		return borrow_money;
	}

	public void setBorrow_money(String borrow_money) {
		this.borrow_money = borrow_money;
	}

	public String getBorrow_status() {
		return borrow_status;
	}

	public void setBorrow_status(String borrow_status) {
		this.borrow_status = borrow_status;
	}

	public String getLeftday() {
		return leftday;
	}

	public void setLeftday(String leftday) {
		this.leftday = leftday;
	}

	public String getPagetype() {
		return pagetype;
	}

	public void setPagetype(String pagetype) {
		this.pagetype = pagetype;
	}

	
}
