package com.p2p.pojo;

import com.baomidou.mybatisplus.annotations.TableField;

/**
 * 活动表
 * 曾林
 * 2017-11-14
 * */
public class Activity {
	@TableField("at_id")
	private Integer atid;//主键ID
	@TableField("at_title")
	private String attitle;//活动标题
	@TableField("at_intgard")
	private Integer atintgard;//奖励值
	@TableField("at_content")
	private String atcontent;//活动内容
	@TableField("at_imag")
	private String atimag;//活动代表图片
	@TableField("at_starttime")
	private String atstarttime;//活动开始时间
	@TableField("at_endtime")
	private String atendtime;//活动结束时间
	@TableField("at_status")
	private Integer atstatus;//活动状态
	
	public Activity() {
		super();
	}

	public Activity(Integer atid, String attitle, Integer atintgard, String atcontent, String atimag,
			String atstarttime, String atendtime, Integer atstatus) {
		super();
		this.atid = atid;
		this.attitle = attitle;
		this.atintgard = atintgard;
		this.atcontent = atcontent;
		this.atimag = atimag;
		this.atstarttime = atstarttime;
		this.atendtime = atendtime;
		this.atstatus = atstatus;
	}

	@Override
	public String toString() {
		return "Activity [atid=" + atid + ", attitle=" + attitle + ", atintgard=" + atintgard + ", atcontent="
				+ atcontent + ", atimag=" + atimag + ", atstarttime=" + atstarttime + ", atendtime=" + atendtime
				+ ", atstatus=" + atstatus + "]";
	}

	public Integer getAtid() {
		return atid;
	}

	public void setAtid(Integer atid) {
		this.atid = atid;
	}

	public String getAttitle() {
		return attitle;
	}

	public void setAttitle(String attitle) {
		this.attitle = attitle;
	}

	public Integer getAtintgard() {
		return atintgard;
	}

	public void setAtintgard(Integer atintgard) {
		this.atintgard = atintgard;
	}

	public String getAtcontent() {
		return atcontent;
	}

	public void setAtcontent(String atcontent) {
		this.atcontent = atcontent;
	}

	public String getAtimag() {
		return atimag;
	}

	public void setAtimag(String atimag) {
		this.atimag = atimag;
	}

	public String getAtstarttime() {
		return atstarttime;
	}

	public void setAtstarttime(String atstarttime) {
		this.atstarttime = atstarttime;
	}

	public String getAtendtime() {
		return atendtime;
	}

	public void setAtendtime(String atendtime) {
		this.atendtime = atendtime;
	}

	public Integer getAtstatus() {
		return atstatus;
	}

	public void setAtstatus(Integer atstatus) {
		this.atstatus = atstatus;
	}
	
}
